export type TranslationKey = keyof typeof translations.tr;

const SUPPORTED_LANGS: Lang[] = ['tr', 'en', 'ko', 'ja', 'pt', 'es'];

function getDeviceLanguage(): Lang {
  try {
    const locale =
      Platform.OS === 'ios'
        ? NativeModules.SettingsManager?.settings?.AppleLocale ||
          NativeModules.SettingsManager?.settings?.AppleLanguages?.[0]
        : NativeModules.I18nManager?.localeIdentifier;
    if (locale) {
      const code = locale.substring(0, 2).toLowerCase();
      const match = SUPPORTED_LANGS.find(l => l === code);
      if (match) return match;
    }
  } catch {}
  return 'en';
}

let cachedLang: Lang | null = null;
let listeners: Array<(lang: Lang) => void> = [];

export async function initLanguage(): Promise<Lang> {
  const stored = await AsyncStorage.getItem(LANG_KEY);
  cachedLang = (SUPPORTED_LANGS.includes(stored as Lang)) ? stored as Lang : getDeviceLanguage();
  return cachedLang;
}

export function getLang(): Lang {
  return cachedLang || 'tr';
}

export async function setLang(lang: Lang) {
  cachedLang = lang;
  await AsyncStorage.setItem(LANG_KEY, lang);
  listeners.forEach(fn => fn(lang));
}

export function toggleLang() {
  const idx = SUPPORTED_LANGS.indexOf(getLang());
  const next = SUPPORTED_LANGS[(idx + 1) % SUPPORTED_LANGS.length];
  setLang(next);
}

export function useT() {
  const [lang, setLangState] = useState<Lang>(getLang());

  useEffect(() => {
    initLanguage().then(l => setLangState(l));
    const listener = (l: Lang) => setLangState(l);
    listeners.push(listener);
    return () => {
      listeners = listeners.filter(fn => fn !== listener);
    };
  }, []);

  return { t: translations[lang], lang, toggleLang };
}
